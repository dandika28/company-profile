package com.mikatama.cp.bean;

public class ContentHowWork {

	private int id;
	private String title;
	private String content;
	private String icon;
	private String iconColor;
	private String process;
	private String additionalClass;

	public String getAdditionalClass() {
		return additionalClass;
	}

	public void setAdditionalClass(String additionalClass) {
		this.additionalClass = additionalClass;
	}



	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public String getIconColor() {
		return iconColor;
	}

	public void setIconColor(String iconTitle) {
		this.iconColor = iconTitle;
	}

	public String getProcess() {
		return process;
	}

	public void setProcess(String process) {
		this.process = process;
	}
	
	

}
